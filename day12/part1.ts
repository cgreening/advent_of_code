const directions = ["N", "E", "S", "W"];
const directionIncrements: { [key: string]: { x: number; y: number } } = {
  N: { x: 0, y: 1 },
  E: { x: 1, y: 0 },
  S: { x: 0, y: -1 },
  W: { x: -1, y: 0 },
};
let directionIndex = 1;
let xpos = 0;
let ypos = 0;

for (const instruction of instructions) {
  // default (for forward instruction)
  let commandedDirection = directions[directionIndex];
  let commandedDistance = 1;
  switch (instruction.instruction) {
    case "L": {
      directionIndex =
        (directionIndex - instruction.count / 90 + directions.length) %
        directions.length;
      commandedDirection = directions[directionIndex];
      commandedDistance = 0;
      break;
    }
    case "R": {
      directionIndex =
        (directionIndex + instruction.count / 90 + directions.length) %
        directions.length;
      commandedDirection = directions[directionIndex];
      commandedDistance = 0;
      break;
    }
    case "F": {
      commandedDistance = instruction.count;
      break;
    }
    default: {
      commandedDirection = instruction.instruction;
      commandedDistance = instruction.count;
    }
  }
  xpos += directionIncrements[commandedDirection].x * commandedDistance;
  ypos += directionIncrements[commandedDirection].y * commandedDistance;
}

